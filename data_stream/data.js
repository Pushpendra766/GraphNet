function generateEdges(length) {
  let edges = [];
  for (let i = 0; i < length; i++) {
    const nod = Math.random() * 9 + 1;
    for (let j = 0; j < nod; j++) {
      let edge = {
        from: i,
        to: parseInt(Math.random() * (length - 1)),
      };
      edges.push(edge);
    }
  }
  console.log(edges);
}

